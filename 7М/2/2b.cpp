#include <iostream>
using namespace std;
int main()
{
    int a,b;
    cout <<"Enter 2 numbers"<<endl;
    cin >>a>>b;
    if (a>b)
    {
        cout <<"1"<<endl;
    }
    if (a<b)
    {
        cout <<"2"<<endl;
    }
    if (a==b)
    {
        cout <<"0"<<endl;
    }
    return 0;
}
